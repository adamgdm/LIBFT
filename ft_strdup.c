#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;
	int		l;

	i = 0;
	l = ft_strlen(src);
	dest = (char *)malloc(sizeof(char) * l + 1);
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
